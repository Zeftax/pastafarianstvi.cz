			Husí kůže
		</h4>
		<p>
			<i>Klam:</i> Evoluční propaganda by vás sváděla k přesvědčení že husí kůže je atavistická, nyní bezvýznamná, odpověď na nouzi - ať již emoční či spojenou s počasím - která měla kdysi naježit chlupy našich předků, díky čemuž vypadali větší a děsivější.
		</p>
		<p>
			<i>Fakt:</i> Husí kůže je chytře skrytá vlastnost která umožnila zvýšený vztlak poté co pirát spadl do studené vody. Pouze tím, že se objevila, zvýšila povrch a tudíž i vztlak. To pirátům umožnilo lépe plout - něco co se našim předkům velmi hodilo, jelikož občas byli bez lodi. Ano, husí kůže se může zdát jako zakrnělý reflex, ale ve skutečnosti to je společnost jenž se změnila.
