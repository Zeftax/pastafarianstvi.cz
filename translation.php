			Pirátí, jak již víte, jsou jeho vyvolení lidé. Ale i přesto jejich počty klesají už od 19. století. V důsledku toho shledáváme že globální oteplování, zemětřesení, hurikány, a ostatní přírodní pohromy jsou přímým důsledkem klesajících počtů pirátů. Pro ilustraci této skutečnosti jsem přiložil známý graf z nedávné studie.
