			LŠM proti ostatním náboženstvím
		</h3>
		<p>
			Konverzace o podpůrcích inteligentního designu, bez ohledu na to jak krátká a klamná, nakonec vždy vede k diskuzi o Bohu a náboženství. Je důležité říct dopředu že Církev Létajícího Špagetového Monstra je mírumilovné náboženství - nejspíše nejmírumilovnější ze všech. Ale můžeme to dokázat? Abychom prozkoumali toto tvrzení, podívejme se na náboženství a násilí napříč historií, obzvláště s ohledem na válku a smrt.
