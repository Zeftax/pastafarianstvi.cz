			Špagetobožstvo
		</h3>
		<p>
			I když jsem evoluci  na těchto stránkách víceméně zdecimoval, je důležité zmínit že existuje i spousta důvěryhodných důkazů podporujících evoluci. Nikdo nemůže zpochybnit fosilní záznam který ukazuje jasné a postupné změny druhů za čas (i když s iritujícími mezerami - a já se vás ptám, kdo <i>je</i> tam dal?). A vskutku by se zdálo že ve světě existují výběrčí síly, například když ožralové lezou do silnice a <span class="tooltip">jsou poté zasaženi auty<span class="tooltiptext">Také, George W. Bush je nápadně podobný šimpanzovi.</span>.
		</p>
		<p>
			Netvrdíme že evoluce <i>nemůže</i> existovat, jen že je naváděna jeho špagetovým výrůstkem. A naše špagetobožstvo je extrémně skromné. Z nějakého důvodu podstoupil veliké obtíže aby nás přesvědčil že evoluce je opravdová - maskujíc prominentní roli pirátů v našich počátcích, a dělajíc opice důležitější než ve skutečnosti jsou, jelikož většinu času jen pohlížejí zpoza opony a vyhýbají se záři reflektorů.
