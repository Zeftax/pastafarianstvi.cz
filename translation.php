		</p>
		<p>
			Nebudu ale na vědce s ptakopyskem tlačit, jelikož to je samozřejmě náročný oříšek na rozlousknutí. Ale jsem si jistý že jsou stovky vědců kteří se právě teď snaží získat definitivu nesmyslným hledáním evolučního významu tohoto směšného stvoření. K ptakopysku si ještě dovolím představit alternativní teorii kterou jsem vymyslel: Létající Špagetové Monstrum stvořilo ptakopyska protože, narozdíl od vědců, má smysl pro humor. Je to nepravděpodobné znamení od boha - a dokud mi někdo neprokáže opak, tak to je má teorie.
		</p>
		<p>
			Nyní se obrátím na více běžné a nudné příklady přirozeného výběru, které poté rozsekám na proužky. Nechť se podíváme na fascinující případ bakterií. Je dobře známo že antibiotika jsou používána k léčení mnoha nemocí způsobených bakteriemi, a je stejně tak dobře známo že většina bakterií (například <span class="tooltip">stafylokok<span class="tooltiptext">Většina vědců jsou zvrhlíci a používají latinské názvy aby ukryli ten fakt, že přeloženo do češtiny stafylokok znamená "mocný penis"</span></span>) si časem vyvine imunitu vůči těmto antibiotikům. Když se na případ stafylokoka podíváme z blízka, tak zjistíme že roku 1929 <span class="tooltip">Sir Alexander Fleming<span class="tooltiptext">Žádná spojitost se Sirem Eltonem Johnem</span></span> poprvé pozoroval že ve staré petriho misce se na agaru usadila bakterie <span class="tooltip">penicillium<span class="tooltiptext">Český význam je "mnoho malých penisů".</span></span>, a že tato plíseň znemožnila stafylokokům se usadit na této petriho misce. Sir Alexander Fleming, nebo "F-Man" jak mů říkala královna, izoloval penicillium a vytvořil penicillin, který se poté stal známý jako zázračný lék proti mnoha nemocem, především pohlavním. Ale postupně se penicilin stal ve své přírodní formě zbytečným. Vědci vám řeknou že ta bakterie - která se množí rychleji než činčila v továrně na viagru - si časem vyvinula odolnost vůči přírodnímu penicilinu, a že přirozený výběr způsobil rozšíření tohoto odolného kmene v přírodě. Toto je prachsprostá lež, kterou za chvíli rozdrtím.
		</p>
		<p>
			Když se podíváme na bakterie které se stanou odolnými vůči antibiotikům, hmyz který se stane odolným vůči insekticidům, nebo dokonce HIV které se stane odolné vůči antivirotikům, tak vidíme fascinující spojení mezi "přirozeným výběrem" a "odolností". Ale co zde doopravdy vidíme? Rád bych navrhl že ve skutečnosti nemění svůj genotyp, ale mění svůj <i>názor</i>. Zkráceně, jsou chytřejší. Když přijdu k vám domů na návštěvu a vy mi dva dny po sobě na jídlo dáte toust s hovnem, tak se půjdu třetí den naobědvat do McDonald's. Je to tak jednoduché. Nenechte vědce a jejich veliké falické bakteriální názvy aby vám řekli cokoliv jiného. Nejsou zdaleka tak chytří jak předstírají že jsou, bez ohledu na to jak moc se snaží ponížit takzvané nižší životní formy.
		</p>
		<p>
			Jeden další příklad přirozeného výběru by mohl stačit k tomu, abychom tento mýtus konečně položili do rakve. Vědci poukazují na "umělý výběr" aby ukázali že lidé, tím že poskytnou vlastní sadu výběrových sil, mohou napodobovat síly přírody. Toto opakovaně vidíme v akcích takzvaných "množičů", kteří prý přivedli nesmírné změny v rostlinách i zvířatech. Můžeme se například podívat na všemožná plemena psů, kde vědci tvrdí že všechny druhy psa vzešli z jednoho společného zdroje: předáka vlka. Očekávají že uvěříme že muž z této divoké bestie vyšlechtil rozmanitý výběr druhů - takový čtyřnozí surovci jako je čivava, jezevčík, pudl a buldok - všichni z nich s námi byli od nepaměti. Tento množírenský mýtus se zdá být druhem propagandy, možná předložen aktivisty proti Inteligentnímu designu, ikdyž si konverzaci o Inteligentním designu ponechám pro pozdější kapitolu. Jak máme věřit takovým tvrzením jako "nejlepší přítel člověka" když je každému pozorovateli jasné že každé plemeno bylo umístěno na tuto planetu aby sloužilo nějakému <i>účelu</i>. Já bych za sebe ukázal na LŠM jako tvůrce psů, ikdyž zde jsou pádné důkazy o tom že by Bůh (kdyby bylo někdy potvrzeno že existuje) mohl být zodpovědný za jejich stvoření. Neslouží koneckonců vlčáci k tomu aby nás chránili, možná dokonce před jejich "předky" vlky? Nebyly čivavy a pudlové umístěni na tuto planetu abychom ze sebe mohli mít lepší pocit? Myslím si že je bez pochyb jasné to, že inteligentní stvořitel umístil na zemi všechna stvoření aby sloužila člověku. A evoluce ani nebyla pořádně vynalezena do 19. století. Je to dost času abyste z divokého vlka záskali labradorského retrívra? Nemyslím si.
		</p>
		<p>
			Pokud vám tento argument není dost, tak se alespoň zamyslete nad tímto posledním příkladem, který pojednává o rodech zeleniny. Když se podíváme na zdomácnělé zelí, brokolici, kapustu, květák, a růžičkovou kapustu, máme snad tvrdit, i kdyby pocházeli ze společného prastarého divokého zelí, že výběr, ať už přírodní či umělý, to je jedno, nemohl za posledních pár tisíc let vytvořit něco lepšího? Odpověď je ve zkroucených tvářích jakéhokoliv dítěte s růžičkovou kapustou v jeho nebo její puse. Další zásah proti evoluci.
		</p>
		<h3>
			Z pirátů na lidí
		</h3>
		<p>
			Jakákoliv diskuze o evoluci nás nakonec zavede k nám samotným. Lidé zde byli tak dlouho jak si jen pamatujeme, ale i přesto vám evolucionisté budou tvrdit že jsme tu nebyli. Budou vám tvrdit že lidé a šimpanzi spolu sdíleli před nějakými pěti miliony let společného předka, a že jsme se od nich "oddělili" a časem jsme vynalezli raketoplán, zatímco šimpanzi od té doby vynalezli pouze klacek. Aby obhájili tuto hypotézu, tak nám vědci tvrdí že s šimpanzi sdílíme 95% našeho DNA, ale <span class="tooltip">s piráty sdílíme 99,9% našeho DNA<span class="tooltiptext">Přijde mi poněkud podezřelé že učebnice biologie jen zřídka zmiňují tento fakt</span></span>. A já se vás ptám, kdo je pravděpodobněji společný předek? A nejsou snad piráti vybranými lidmi LŠM? Proč trávíme tolik času tím, že se bavíme o něčem co se nestalo, když nám LŠM houpe jeho špagetovými výrůstky přímo pod nosem?
		</p>
