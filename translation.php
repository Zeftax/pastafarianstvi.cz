		</p>
		<p>
			<i>Fakt:</i> Slepé střevo byl vychytaný vnitřní vak který sloužil k úschově pirátova zlata. Je také inspirací pro frázi "Tak to vyplivni", což piráti vyžadovali po poražených pirátech poté co se dostali na palubu jejich lodí.
