		</p>
		<p>
			Je poněkud podivné že evoluce je vyčleňována jako "pouze teorie" když zde je tolik dalších základních vědeckých přesvědčení které zůstávají nepotvrzeny, ale i přesto jsou učeny jako fakt. Námitky proti učení evoluce pouze rozvedly tuto myšlenku: <em>Alternativní teorie musí být učeny abychom myslím našich mladých studentů mohli dát obšírné základy</em>. Zastánci inteligentního designu přesvědčivě a opodstatněně argumentují že pokud teorie nebyla potvrzena, tak je stejně dobrá jako každá jiná navrhovaná teorie.
		</p>
		<p>
			Vezměme například gravitaci: sílu přitahování mezi masivními tělesy. Víme toho spoustu o vlastnostech gravitace, ale nevíme nic o tom co ji způsobuje. Proč jsou částice vzájemně přitahovány? Když se podívame na literaturu, zjistíme že spousta materiálu pojednává o vlastnostech gravitace, ale jen velmi málo pojednává o základním zdroji tohoto přitahování. Dokud nemáme potvrzenou odpověď na tuto otázku, tak je poněkud nezodpovědné učit studenty něco, co je konec konců pouze teorie. Ale, pokud už je nutno teorii gravitace probírat. Tak by bylo rozumné dát všem teoriím stejnou hodinovou dotaci, jelikož žádná eorie ještě nebyla potvrzena ani vyvrácena. Tudíž formálně předkládám že Létající Špagetové Monstrum stojí za touto podivnou a často nepochopenou silou.
		</p>
