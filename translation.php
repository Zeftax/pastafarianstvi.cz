			Kostrč
		</h4>
		<p>
			<i>Klam:</i> Evolucionisté tvrdí že kostrč, která nemá žádný zjištěný účel, je podivuhodný zůstatek po větší kosti která kdysi mohla tvořit ocas, stejný jako mají ostatní primáti.
		</p>
		<p>
			<i>Fakt:</i> Lidé s ocasy... to se vědci na něčem sjeli? Nemohla kostrč sloužit jinému účelu? Důkladně jsem prozkoumal tento problém a porovnal jsem kostrč k ostatním podivným kostnatým výrůstkům ve zvířatech a literatura mě zavedla k jedinému závěru. Spousta zvířat má na hlavách rohy, a o těch se neuvažuje jako o zbytcích po větší kosti, nejspíše proto že narozdíl od kostrče rohy mají účel i dnes. Ale co když to je kultura, jenž učinila kostrč zbytečnou? Když se na kostrč podíváte zblízka, tak zjistíte že je poněkud podobná <i>rohu</i>, což je struktura mnoha zvířaty používána k boji. Já tedy navrhuji že kostrč není zakrnělý ocas nýbrž efektivní, i když podivně umístěný nástroj pro boj.
		</p>
		<p>
			Představuji si dva protivníky jak bojují o ženy nebo jimi zvolenou jeskynní nemovitost tak, že na sebe naběhnou zadky vpřed, podobně jako bojují srnci s jejich rohy. Nazval jsem to zadko-bojování. Když se nad tím zamyslíte tak to dává smysl, jelikož by jim to uvolnilo ruce ve kterých by poté mohli nést co by chtěli - nejpravděpodobněji jídlo nebo kamení.
		</p>
		<p>
			Jako další důkaz toho že kostrč bývala útočným nástrojem, a že část jejího užitku kulturně přežila přes všechna ta léta, zvažte jak rychle někdo uteče když se proti nim rozeběhnete prdelí vpřed. Navrhuji aby tato hypotéza byla otestována kýmkoliv kdo s ní nesouhlasí - zkuste příštích pár dnů do kohokoliv koho potkáte <span class="tooltip">vrazit zadkem<span class="tooltiptext">Ženám není doporučováno tuto metodu zkoušet v okolí zvrhlých mužů</span></span>. Jsem si jistý že většina, ne li všechny, z těchto obětí bude přinejmenším velmi vyděšená. Nevidím žádný jiný důvod proč by se toto dělo, než že bychom podvědomě věděli že kostrč je zbraň, nikoliv zakrnělý ocas.
		</p>
		<h4>
			Ta další zakrnělá vlastnost
		</h4>
		<p>
			<i>Klam:</i> Lidský genom skýtá důkazy že lidé nebyli stvořeni <span class="tooltip">ex nihilo<span class="tooltiptext">Nihila byly občerstvení ranných římanů, ranný předchůdce doritos. Ve své podstatě by se tento termín dal přeložit jako "z doritos".</span></span>, ale naopak se museli vyvinout postupně, stejně jako všechna ostatní zvířata. Jako důkaz vědci ukazují na spousty nefunkčního DNA, včetně mnoha neaktivních "pseudo genů" které byly funkční u některých z našich předchůdců, ale nejsou u nás. Jeden často uváděný příklad je výroba vitaminu C, tento gen je aktivní u všech členů čeledi primátů kromě jednoho: člověka. Vědci tvrdí že to je důkaz společné minulosti, ale já nemohu přijít na to proč.
