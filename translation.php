		<h3>
			Evoluce je sexy
		</h3>
		<p>
			Konečně jsme se dostali k "pohlavnímu výběru" jak jsem před chvílí slíbil. Základní myšlenkou za pohlavním výběrem je to, že jedno z pohlaví, zpravidla samice, si aktivně vybírá členy opačného pohlaví se kterými se <span class="tooltip">pářit<span class="tooltiptext">Bonk!</span></span>, dle nějakých kritérií, a tím vytváří výběrový nátlak na svůj druh jako takový. Pohlavní evoluce vysvětluje jasné zbarvení samců ptáků, impozantní duely mezi samci beranů, jelenů, losů, a <span class="tooltip">dalších kopytnatců<span class="tooltiptext">Berani, jeleni, losi, apod.</span></span>, a také vysoké procento Hummerů řízených krátkými hnusnými muži. Zkrátka, pohlavní výběr závisí na úspěchu některých individuálů nad ostatními příslušníky stejného pohlaví, zatímco přirozený výběr je <i>nezávislý na pohlaví</i>. V zájmu pokroku navrhuji aby sněmovna schválila zákon který by zakázal tuto zaostalou a sexistickou praktiku.
