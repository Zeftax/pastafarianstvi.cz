		<p>
			Ale i přesto že se drží zpátky, tak nechť nikdo nepochybuje o tom, že Církev Létajícího Špagetového Monstra je nejen přelomové náboženství, ale je také podporováno tvrdou vědou, což z něj činí bezpochybně nejpravdivější teorii předloženou v dějinách lidstva. Abych udělal pointu tak se podívám na moderní problém globálního oteplování.
		</p>
